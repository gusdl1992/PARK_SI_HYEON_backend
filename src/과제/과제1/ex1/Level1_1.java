public class Level1_1 {
	
	public static void main(String[] args) {
		System.out.println(" >> Level1_1 answer >> \n");
		/* 문제풀이 위치 */

		boolean var1 = true;
		System.out.println("boolean = " + var1);	// boolean
		byte var2 = 100;
		System.out.println("byte = " + var2);	// byte
		char var3 = 'A';
		System.out.println("char = " + var3);	// char
		short var4 = 30000;
		System.out.println("short = " + var4);	// short
		int var5 = 2000000000;
		System.out.println("int = " + var5);	// int
		long var6 = 4000000000L;
		System.out.println("long = " + var6);	// long
		float var7 = 3.1231232F;
		System.out.println("float = " + var7);	// float
		double var8 = 3.123123123;
		System.out.println("double = " + var8);	// double


		/* ----------- */
	}
}
