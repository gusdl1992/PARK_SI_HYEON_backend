package 박시현개인연습.textRPG;

public class Monster {

    private String name;
    private int hp;
    private int str;
    private int def;
    private int spd;

    Monster(){}
    Monster(String name , int hp , int str , int def , int spd){
        this.name = name;
        this.hp = hp;
        this.str = str;
        this.def = def;
        this.spd = spd;
    }

}
